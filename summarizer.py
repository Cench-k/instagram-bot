def summarize(title: str, body: str, num_points: int = 4) -> list[str]:
    """
    기사 제목과 본문을 받아 카드뉴스용 핵심 포인트 리스트 반환.
    각 포인트는 한 줄, 20자 이내 권장.
    """
    prompt = f"""다음 뉴스 기사를 카드뉴스용으로 요약해주세요.
규칙:
- 핵심 포인트 {num_points}개만 추출
- 각 항목은 한 문장, 30자 이내
- 번호나 불릿 기호 없이 텍스트만
- 한국어로 작성

제목: {title}
본문:
{body[:2000]}

결과 형식 (각 줄에 하나씩):
포인트1
포인트2
포인트3
포인트4"""

    response = _model.generate_content(prompt)
    lines = [line.strip() for line in response.text.strip().split("\n") if line.strip()]
    return lines[:num_points]
